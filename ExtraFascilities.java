package ProjectLab;

public class ExtraFascilities {
	private boolean isFood;
	private boolean isMuseum;
	private boolean foodAmount;
	
	
	public boolean isFood() {
		return isFood;
	}
	public void setFood(boolean isFood) {
		this.isFood = isFood;
	}
	public boolean isFoodAmount() {
		return foodAmount;
	}
	public void setFoodAmount(boolean foodAmount) {
		this.foodAmount = foodAmount;
	}
	public boolean isMuseum() {
		return isMuseum;
	}
	public void setMuseum(boolean isMuseum) {
		this.isMuseum = isMuseum;
	}
}
